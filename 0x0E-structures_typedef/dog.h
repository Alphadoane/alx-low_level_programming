#ifndef DOG_H
#define DOG_H

/**
 * struct dog - A structure to store information about a dog
 * @name: Pointer to a string representing the dog's name
 * @age: Float representing the dog's age
 * @owner: Pointer to a string representing the dog's owner
 */
struct dog
{
char *name;
float age;
char *owner;
};

#endif /* DOG_H */ 
